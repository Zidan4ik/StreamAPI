package examples.lambda;

public class Triangle {
    private int katetFirst;
    private int katetSecond;

    public Triangle(int katetFirst, int katetSecond) {
        this.katetFirst = katetFirst;
        this.katetSecond = katetSecond;
    }

    public int getKatetFirst() {
        return katetFirst;
    }
    public int getKatetSecond() {
        return katetSecond;
    }
}
